                    <h1 class="article_title">Mint Strawberry Brownies</h1>
                </div>



                <article class="article_recipe_content-block">
                    <p>Who doesn’t love the combination of rich, intense chocolate and cooling refreshing mint? These brownies are minty, they’re chocolatey and they taste absolutely delicious! Even better, they're vegan, gluten free and paleo-friendly! This recipe made in partnership with the talented <a href="https://www.instagram.com/nm_meiyee/">Mei Yee</a>. </p>
                    <p>Recipe makes : 16 servings</p>
                    <p>Prep time : 5 mins</p>
                    <p>baking/cooking time : 20mins</p>
                    <p style="text-align: left;">﻿﻿</p>
                    <p style="text-align: left;"><em>﻿<img src="assets/images/goldjjj.png" alt=""></em></p>
                    <p> </p>
                    <h3>
                        <strong>Ingredients</strong> </h3>
                    <ul>
                        <li>192 g gluten free flour </li>
                        <li>1/2 tsp baking powder </li>
                        <li>60 ml water </li>
                        <li>150 g coconut sugar </li>
                        <li>2 flax eggs (2 tbsp flaxseed + 6 tbsp water) </li>
                        <li>1/2 tsp salt </li>
                        <li>1 tsp vanilla </li>
                        <li>
                            <a href="../../products/dark-mint.html" target="_blank" rel="noopener noreferrer">160g organic dark mint chocolate</a> </li>
                        <li>3 tbsp cocoa powder </li>
                        <li>170 g coconut oil or canola oil </li>
                    </ul>
                    <p><strong>Topping</strong> </p>
                    <ul>
                        <li><a href="../../products/dark-mint.html" target="_blank" rel="noopener noreferrer">80g organic dark mint chocolate </a></li>
                        <li>1 1/2 tbsp coconut oil </li>
                        <li>85 g fresh strawberry </li>
                    </ul>
                    <h3> <strong>Directions</strong>
                    </h3>
                    <ol>
                        <li>Preheat your oven to 325 degrees F (165 degrees C) </li>
                        <li>In a large bowl combine flour, cocoa powder, sugar, baking powder, salt and set a side.</li>
                        <li>Melt chocolate bar, stir in oil.</li>
                        <li>Pour chocolate oil mixture to dry mixture, add flax eggs and pour in milk and stir with a wooden spoon until just combined.</li>
                        <li>Spread mixture into a greased 8x8inch pan.</li>
                        <li>Bake at 325 degrees F (165 degrees C) for 20mins until knife inserted into center of pan comes out clean. Allow to cool completely. </li>
                        <li>Melt 80g chocolate with coconut oil. Spread chocolate on brownies and top the brownies with all of the the strawberry. Place in the fridge to fully harden.</li>
                        <li>Slice and enjoy. </li>
                    </ol>
